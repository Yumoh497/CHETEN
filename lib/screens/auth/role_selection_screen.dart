                  'Welcome to Cheteni',
                  style: TextStyle(
                    fontSize: 32,
                    fontWeight: FontWeight.bold,
                    color: Colors.white,
                  ),
                  textAlign: TextAlign.center,
                ),
                const SizedBox(height: 12),
                const Text(
                  'How would you like to use Cheteni?',
                  style: TextStyle(
                    fontSize: 18,
                    color: Colors.white70,
                  ),
                  textAlign: TextAlign.center,
                ),
                const SizedBox(height: 50),
                _buildRoleCard(
                  context,
                  'Customer',
                  'Order fresh fruits & vegetables',
                  Icons.shopping_cart,
                  Colors.blue,
                  () => Navigator.pushNamed(context, '/customer_login'),
                ),
                const SizedBox(height: 16),
                _buildRoleCard(
                  context,
                  'Driver',
                  'Deliver orders and earn',
                  Icons.delivery_dining,
                  Colors.orange,
                  () => Navigator.pushNamed(context, '/driver_login'),
                ),
                const SizedBox(height: 16),
                _buildRoleCard(
                  context,
                  'Agent',
                  'Manage operations & orders',
                  Icons.business,
                  Colors.purple,
                  () => Navigator.pushNamed(context, '/agent_login'),
                ),
                const SizedBox(height: 30),
                TextButton(
                  onPressed: () {
                    Navigator.pushNamed(context, '/admin_login');
